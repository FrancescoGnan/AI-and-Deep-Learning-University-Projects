def create_dataset(coeffs, z_range=(-1,1), sample_size=10, sigma=0.1, seed=42):
    random_state = np.random.RandomState(seed)
    z_min, z_max = z_range
    X = random_state.uniform(z_min, z_max, (sample_size))
    X = np.array([X**i for i in range(5)])
    y_hat = np.dot(coeffs,X)
    y = y_hat + random_state.normal(0.0, sigma, (sample_size))
    return X, y

