def polynomial(z, coeffs):
  return coeffs[0] + z * coeffs[1] + z**2 * coeffs[2] + z**3 * coeffs[3] + z**4 * coeffs[4]

def plot_polynomial(coeffs, z_range, color='b'):
  z_min, z_max = z_range
  z = np.linspace( z_min, z_max, 100 )
  plt.figure()
  plt.plot(z, [polynomial(i,coeffs) for i in z], color=color, label='Exact ploynomial')
  plt.xlabel('z')
  plt.ylabel('p(z)')
  plt.legend()
  plt.savefig("Ex1.pdf")

